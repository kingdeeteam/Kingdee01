using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ZGT2
{
    class Person
    {
        private string id;

        public string Id
        {
            get { return id; }
            set { id = value; }
        }
        private string name;

        public string Name
        {
            get { return name; }
            set { name = value; }
        }
        private string room;

        public string Room
        {
            get { return room; }
            set { room = value; }
        }
        private string money;

        public string Money
        {
            get { return money; }
            set { money = value; }
        }
    }
}
